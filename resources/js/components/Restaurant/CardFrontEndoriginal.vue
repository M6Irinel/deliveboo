<template>
  <div class="grid-12 gap-5" id="lpcards">
    <router-link
      class="
        my-card
        relative
        g-col-12 g-col-6-md
        p-2
        rounded-3
        t-right
        orange-H
        shadow
        decoration-none-H
        overflow-hidden
        lpcard
      "
      :class="[restaurant.restaurant_image ? 'white bg-brown' : 'black']"
      v-for="(restaurant, i) in restaurants"
      :key="i"
      :to="{
        name: 'Plates',
        params: {
          slug: restaurant.user.slug,
        },
      }"
    >
      <div class="my-card-img" v-if="restaurant.restaurant_image">
        <img :src="'./storage/' + restaurant.restaurant_image" alt="" />
      </div>

      <div class="my-card-body flex f-column h-100 lpbody">
        <h3>{{ restaurant.user.name }}</h3>
        <address>{{ restaurant.restaurant_address }}</address>
        <div class="mt-auto">
          <p
            class="inline-block px-1"
            v-for="(typology, e) in restaurant.typologies"
            :key="e"
          >
            {{ typology.name }}
          </p>
        </div>
      </div>
    </router-link>
  </div>
</template>


<script>
// @ts-nocheck
export default {
  name: "CardFrontEnd",

  props: {
    restaurants: {
      required: true,
      type: Array,
    },
  },
  methods: {
    letshover() {
      document.getElementById("lpcards").onmousemove = (e) => {
        for (const card of document.getElementsByClassName("lpcard")) {
          const rect = card.getBoundingClientRect(),
            x = e.clientX - rect.left,
            y = e.clientY - rect.top;

          card.style.setProperty("--mouse-x", `${x}px`);
          card.style.setProperty("--mouse-y", `${y}px`);
        }
      };
    },
  },
  created (){
    this.letshover()
  }
};
</script>


